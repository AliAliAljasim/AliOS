/* ── Internal fd helpers ─────────────────────────────────────────────────── */

/* Close and release one fd of current_task (IF must be 0 when called). */
static void fd_close_one(int i)
{
    task_t *t = current_task;
    if (!t->fd_table[i].used) return;
    t->fd_table[i].used = 0;
    if (t->fd_table[i].type == FD_PIPE) {
        if (t->fd_table[i].pipe_write)
            pipe_close_write(t->fd_table[i].pipe_idx);
        else
            pipe_close_read(t->fd_table[i].pipe_idx);
    }
}

